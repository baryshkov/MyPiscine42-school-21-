#include <stdio.h>

int main(void)
{
	unsigned char a = 12;
	printf("1&(%u << 1 == %u\n)", a, (a << 1));
	printf("%u << 2 == %u\n", a, a << 2);
	printf("%u >> 1 == %u\n", a, a >> 1);
	printf("%u >> 2 == %u\n", a, a >> 2);
	return (0);
}
