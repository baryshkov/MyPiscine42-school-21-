#include <unistd.h>

int main (int argc, char **argv)
{
	int i;
	int f;

	i = 0;
	f = 0;

	while(argv[1][i] == ' ' || argv[1][i] == '\t')
		i++;
	while(argv[1][i] != '\0')
	{
		if (argv[1][i] == ' ' || argv[1][i] == '\t')
			f = 1;
		if (argv[1][i] != ' ' && argv[1][i] != '\t')
		{
			if(f)
			{
				write(1, "   ", 3);
			}
			f = 0;
			write(1, &argv[1][i], 1);
		}
		i++;

	}
	return (0);
}

