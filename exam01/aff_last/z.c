#include <unistd.h>
int		main(int argc, char **argv)
{
	int i;

	i = 0;
	if (argc != 2)
	{
		write(1, "z\n", 2);
		return (0);
	}
	else
	{
		while (argv[1][i])
		{
			if (argv[1][i] == 'z')
			{
				write(1, &argv[1][i], 1);
				break;
			}
			i++;
		}
		write(1, "\n", 2);
	}
	return (0);
}
